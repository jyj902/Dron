	  Gyro_Roll_Input  = (Gyro_Roll_Input * 0.7)  + ((fGyro[1] / 65.5) * 0.3);
	  Gyro_Yaw_Input   = (Gyro_Yaw_Input * 0.7)   + ((fGyro[2] / 65.5) * 0.3);

